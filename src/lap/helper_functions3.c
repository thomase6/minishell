#include "../../inc/lap.h"

int	ft_isalnum_lap(int c)
{
	if ((c >= 'a' && c <= 'z')
		|| (c >= '0' && c <= '9')
		|| (c >= 'A' && c <= 'Z')
		|| c == '_')
		return (1);
	return (0);
}
